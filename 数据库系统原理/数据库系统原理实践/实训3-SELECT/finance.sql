-- use mysql;
drop database if exists finance; 
create database finance;
use finance;

SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;


-- ----------------------------
-- 客户表: client
-- ----------------------------
DROP TABLE IF EXISTS `client`;
CREATE TABLE `client`  (
  `c_id` int(0) NOT NULL COMMENT '客户编号',
  `c_name` varchar(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '客户名称',
  `c_mail` char(30) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NULL DEFAULT NULL COMMENT '客户邮箱',
  `c_id_card` char(18) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '客户身份证',
  `c_phone` char(11) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '客户手机号',
  `c_password` char(20) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '客户登录密码',
  PRIMARY KEY (`c_id`) USING BTREE,
  UNIQUE INDEX `c_id_card`(`c_id_card`) USING BTREE,
  UNIQUE INDEX `c_phone`(`c_phone`) USING BTREE,
  UNIQUE INDEX `c_mail`(`c_mail`) USING BTREE
) ENGINE = InnoDB CHARACTER SET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;


-- ----------------------------
-- 银行卡表： bank_card
-- ----------------------------
DROP TABLE IF EXISTS `bank_card`;
CREATE TABLE `bank_card`  (
  `b_number` char(30) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '银行卡号',
  `b_type` char(20) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NULL DEFAULT NULL COMMENT '银行卡类型',
  `b_c_id` int(0) NOT NULL COMMENT '所属客户编号',
  `b_balance` decimal(10, 2) NULL DEFAULT NULL COMMENT '余额',
  PRIMARY KEY (`b_number`) USING BTREE,
  INDEX `fk_b_c_id`(`b_c_id`) USING BTREE
) ENGINE = InnoDB CHARACTER SET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;


-- ----------------------------
-- 理财产品表: finances_product
-- ----------------------------
DROP TABLE IF EXISTS `finances_product`;
CREATE TABLE `finances_product`  (
  `p_name` varchar(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '产品名称',
  `p_id` int(0) NOT NULL COMMENT '产品编号',
  `p_description` varchar(4000) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NULL DEFAULT NULL COMMENT '产品描述',
  `p_amount` int(0) NULL DEFAULT NULL COMMENT '每份金额',
  `p_year` int(0) NULL DEFAULT NULL COMMENT '理财年限',
  PRIMARY KEY (`p_id`) USING BTREE
) ENGINE = InnoDB CHARACTER SET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

-- ----------------------------
-- 保险产品表: insurance
-- ----------------------------
DROP TABLE IF EXISTS `insurance`;
CREATE TABLE `insurance`  (
  `i_name` varchar(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '保险名称',
  `i_id` int(0) NOT NULL COMMENT '保险编号',
  `i_amount` int(0) NULL DEFAULT NULL COMMENT '保险金额',
  `i_person` char(20) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NULL DEFAULT NULL COMMENT '适用人群',
  `i_year` int(0) NULL DEFAULT NULL COMMENT '保险年限',
  `i_project` varchar(200) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NULL DEFAULT NULL COMMENT '保障项目',
  PRIMARY KEY (`i_id`) USING BTREE
) ENGINE = InnoDB CHARACTER SET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

-- ----------------------------
-- 基金产品表: fund
-- ----------------------------
DROP TABLE IF EXISTS `fund`;
CREATE TABLE `fund`  (
  `f_name` varchar(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '基金名称',
  `f_id` int(0) NOT NULL COMMENT '基金编号',
  `f_type` char(20) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NULL DEFAULT NULL COMMENT '基金类型',
  `f_amount` int(0) NULL DEFAULT NULL COMMENT '每份金额',
  `risk_level` char(20) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '风险等级',
  `f_manager` int(0) NOT NULL COMMENT '基金管理者',
  PRIMARY KEY (`f_id`) USING BTREE,
  UNIQUE INDEX `f_name`(`f_name`) USING BTREE
) ENGINE = InnoDB CHARACTER SET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;


-- ----------------------------
-- 资产表: property
-- ----------------------------
DROP TABLE IF EXISTS `property`;
CREATE TABLE `property`  (
  `pro_id` int(0) NOT NULL COMMENT '资产编号',
  `pro_c_id` int(0) NOT NULL COMMENT '客户编号',
  `pro_pif_id` int(0) NOT NULL COMMENT '商品编号-引用自三种产品的id',
  `pro_type` int(0) NOT NULL COMMENT '商品类型:1-理财,2-保险,3-基金',
  `pro_status` char(20) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL COMMENT '商品状态:可用/冻结',
  `pro_quantity` int(0) NULL DEFAULT NULL COMMENT '商品份数',
  `pro_income` int(0) NULL DEFAULT NULL COMMENT '商品收益',
  `pro_purchase_time` date NULL DEFAULT NULL COMMENT '购买时间',
  PRIMARY KEY (`pro_id`) USING BTREE,
  INDEX `fk_pro_c_id`(`pro_c_id`) USING BTREE
) ENGINE = InnoDB CHARACTER SET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;


